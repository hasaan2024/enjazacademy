(function() { // Use an IIFE to run the code immediately
  // Function to get language from URL query parameter
  const getLangFromURL = () => {
    const params = new URLSearchParams(window.location.search);
    return params.get('lang');
  };

  function setLanguage(lang = "ar") {
    const currentTranslations = translations[lang];
    if (!currentTranslations) return;

    // Set document direction and language
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Update UI elements like flags and buttons
    const currentFlag = document.getElementById('currentFlag');
    if (currentFlag) {
      const flagMap = {
        ar: 'assets/images/egypt.jpg',
        en: 'assets/images/usa.jpg',
        de: 'assets/images/germany.jpg',
      };
      currentFlag.src = flagMap[lang] || '';
      currentFlag.alt = currentTranslations[`lang_${lang}`] || lang;
    }

    const currentLangText = document.getElementById('currentLangText');
    if (currentLangText) {
      // Dynamically update the data-key to get the correct translation for the current language's name
      currentLangText.setAttribute('data-key', `lang_${lang}`);
    }

    // Update all internal links to include the language parameter
    document.querySelectorAll('a[href]').forEach(link => {
      const href = link.getAttribute('href');
      // Only modify relative links that don't start with #
      if (href && !href.startsWith('#') && !href.startsWith('http') && !href.startsWith('mailto') && !href.startsWith('tel')) {
        try {
          const url = new URL(link.href); // Use the absolute href to handle relative paths correctly
          url.searchParams.set('lang', lang);
          link.href = url.pathname + url.search + url.hash;
        } catch (e) {
          // Ignore invalid URLs
        }
      }
    });

    // Translate all elements with data-key
    document.querySelectorAll("[data-key]").forEach(element => {
      const key = element.getAttribute("data-key");
      const translation = currentTranslations[key];
      if (translation) {
        if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
          element.placeholder = translation;
        } else if (element.tagName === "OPTION") {
          element.textContent = translation; // For select options
        } else {
          element.textContent = translation; // Use textContent for most elements
        }
      }
    });

    // Make the body visible after translations are applied to prevent FOUC
    document.body.style.visibility = 'visible';
  }

  // Language switcher logic
  const langSwitcherBtn = document.getElementById('languageSwitcherBtn');
  const langDropdown = document.getElementById('languageDropdown');
  const langOptions = document.querySelectorAll('.dropdown-item');
  
  langOptions.forEach(option => {
    option.addEventListener('click', (e) => {
      e.preventDefault();
      const newLang = option.getAttribute('data-lang');      
      localStorage.setItem('language', newLang);

      const currentUrl = new URL(window.location.href);
      currentUrl.searchParams.set('lang', newLang);
      
      // Reload the page with the new language parameter
      window.location.href = currentUrl.href;
    });
  });

  // Determine the language
  const urlLang = getLangFromURL();
  const savedLang = localStorage.getItem('language');
  const lang = urlLang || savedLang || 'ar';

  if (!urlLang && savedLang) {
    // This needs to run after the full URL is available
    document.addEventListener('DOMContentLoaded', () => {
      const currentUrl = new URL(window.location.href);
      currentUrl.searchParams.set('lang', savedLang);
      window.history.replaceState({}, '', currentUrl.href);
    });
  }

  setLanguage(lang);
  localStorage.setItem('language', lang);
})(); // End of IIFE
